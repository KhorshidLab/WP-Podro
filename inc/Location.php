	public static function is_podro_city($code){
		return array_search($code, self::get_podro_city());
	}

	public static function get_province_by_code($code) {
		$provinces = self::get_provinces();
		foreach ($provinces as $province) {
			if ($province['code'] == $code) {
				return $province;
			}
		}
		return false;
	}

	public static function get_city_by_name( $city_name ) {
		$cities = self::get_cities();
		foreach ( $cities as $state_id => $cities_in_state ) {
			foreach ( $cities_in_state as $city ) {
				if ( $city['name'] == $city_name ) {
					return $city;
				}
			}
		}
		return false;
	}


	public static function get_cities_by_province_code($code) {
		$province = self::get_province_by_code($code);
		return self::get_cities()[$province['id']];
	}

	public function install_cities() {
		if ( get_option( 'podro _cities_installed' ) ) {
			foreach ( self::get_provinces() as $province ) {
				$term = wp_insert_term(
					$province['name'],
					'state_city',
					[
						'slug' => $province['code'],
						'description' => "استان " . $province['name']
					]
				);

				if ( is_wp_error( $term ) ) {
					continue;
				}

				foreach( self::get_cities_by_province_code($province['code']) as $city ) {
					$term = wp_insert_term(
						$city['name'],
						'state_city',
						[
							'slug' => $city['code'],
							'description' => "شهر " . $city['name']
						]
					);
				}
			}


			update_option( 'podro_cities_installed', true );
		}
	}


	public static function load_cities_callback() {

		if ( ! isset( $_POST['state_id'] ) ) {
			die();
		}

		$state_id = absint( sanitize_text_field($_POST['state_id']) );

		if ( ! $state_id ) {
			die();
		}

		$cities = Location::get_cities_by_province_code( $state_id );

		$type = isset( $_POST['type'] ) && $_POST['type'] == 'billing' ? 'billing' : 'shipping';

		$term_id = WC()->checkout()->get_value( $type . '_city' );

		if ( intval( $term_id ) == 0 ) {
			$term_id = apply_filters( 'pws_default_city', 0, $type, $state_id );
		}

		printf( "<option value='0'>لطفا شهر خود را انتخاب نمایید </option>" );

		foreach ( $cities as $id => $name ) {
			printf( "<option value='%d' %s>%s</option>", $id, selected( $term_id, $id, false ), $name );
		}

		die();
	}

	public static function load_districts_callback() {

		if ( ! isset( $_POST['city_id'] ) ) {
			die();
		}

		$city_id = absint(  sanitize_text_field($_POST['city_id']) );

		if ( ! $city_id ) {
			die();
		}

		$cities = get_terms( [
			'taxonomy'   => 'state_city',
			'hide_empty' => false,
			'child_of'   => $city_id,
		] );

		if ( is_wp_error( $cities ) ) {
			die();
		}

		$cities = apply_filters( 'pws_districts', $cities, $city_id );

		$type = isset( $_POST['type'] ) && sanitize_text_field( $_POST['type'] ) == 'billing' ? 'billing' : 'shipping';

		$term_id = \WC()->session->get( $type . '_district', 0 );

		foreach ( $cities as $city ) {
			$indent = str_repeat( "- ", count( get_ancestors( $city->term_id, 'state_city' ) ) - 2 );
			printf( "<option value='%d' %s>%s</option>", $city->term_id, selected( $term_id, $city->term_id, false ), $indent . $city->name );
		}

		die();
	}
